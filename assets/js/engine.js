  function combine(seed, mods, position) {
    return mods.map(m => position === 'before' ? `${m} ${seed}` : `${seed} ${m}`);
  }

  function bucket(title, items, intent) {
    return { title, intent, items: uniq(items).filter(Boolean).slice(0, 14) };
  }

