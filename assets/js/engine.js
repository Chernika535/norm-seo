  function generateByTopic(seed, platform) {
    seed = (seed || '').trim().toLowerCase();
    if (!seed) return [];
    let buckets;
    switch (platform) {
      case 'seo': buckets = genSEO(seed); break;
      case 'geo': buckets = genGEO(seed); break;
      case 'ats': buckets = genATS(seed); break;
      case 'tiktok': buckets = genSocial(seed, 'tiktok'); break;
      case 'instagram': buckets = genSocial(seed, 'instagram'); break;
      case 'pinterest': buckets = genPinterest(seed); break;
      case 'litres': buckets = genLitres(seed); break;
      case 'podcast': buckets = genPodcast(seed); break;
      default: buckets = [];
    }
    return buckets.length ? buckets.concat(genAudience(seed)) : buckets;
  }

