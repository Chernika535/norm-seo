  /* ---------- Утилиты обработки текста ---------- */

  function tokenize(text) {
    return (text || '')
      .toLowerCase()
      .replace(/ё/g, 'е')
      .replace(/[^\p{L}\p{N}\s\-]/gu, ' ')
      .split(/\s+/)
      .filter(Boolean);
  }

  function sentences(text) {
    return (text || '').split(/[.!?…]+/).map(s => s.trim()).filter(Boolean);
  }

  function isStop(w) { return D.STOPWORDS.has(w) || w.length < 3; }

  function titleCase(s) {
    return s.charAt(0).toUpperCase() + s.slice(1);
  }

  function uniq(arr) { return Array.from(new Set(arr)); }

  // ярлыки тональностей и упаковка версий в {label, text}
  const TONE_LABELS = ['🔥 Вдохновляющая', '💼 Деловая, экспертная', '❓ Интригующая', '💛 Тёплая, личная', '📋 Структурная'];
  function labeled(texts, labels) {
    labels = labels || TONE_LABELS;
    return texts.map((t, i) => ({ label: labels[i] || '', text: t }));
  }

  function atLeastChars(text, minimum, addition) {
    let result = String(text || '').trim();
    while (result.length < minimum) result += ' ' + addition;
    return result;
  }

