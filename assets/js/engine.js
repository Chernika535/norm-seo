  function collectKeywords(mode, platform, value) {
    const set = [];
    // берём только короткие строковые ключи (не длинные тексты/аннотации)
    const add = i => {
      if (typeof i !== 'string') return;
      const s = i.replace(/\s+—\s+\d+×$/, '');
      if (s.length <= 60) set.push(s);
    };
    if (mode === 'topic') {
      generateByTopic(value, platform).forEach(b => b.items.slice(0, 5).forEach(add));
    } else {
      const res = analyzeText(value, platform);
      if (res) res.buckets.forEach(b => b.items.slice(0, 8).forEach(add));
    }
    return uniq(set.filter(s => s && !/не найдено/i.test(s))).slice(0, 26);
  }

